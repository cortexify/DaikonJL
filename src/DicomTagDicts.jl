function getVr(group::UInt16, element::UInt16)
    groupData = get(Dicts, group, nothing)
    elementData = nothing
    vr = nothing

    if (groupData != nothing)
        elementData = get(groupData, element, nothing)
        if (elementData != nothing)
            vr = elementData[1]
        elseif (element == 0) 
            vr = "UL"
        end
    end

    if (vr == nothing)
        groupData = get(DictsPrivate, group, nothing)
        if (groupData != nothing)
            elementData = get(groupData, element, nothing)
            if (elementData != nothing)
                vr = elementData[1]
            end
        end
    end

    if (vr == nothing)
        vr = "OB"
    end

    return vr
end

